def calculate_average(numbers):
  if not numbers:
    return 0
  sum = 0
  for i in range(len(numbers)):
    sum = sum + numbers[i]
  average = sum/len(numbers)
  return average
num = int(input('How many numbers of which you want the average: '))
numbers = []
print("Input the number: ")
for i in range(num):
  n = int(input())
  numbers.append(n)

