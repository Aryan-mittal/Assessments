def count_vowels(string):

    vowels = {'a', 'e', 'i', 'o', 'u'}
    count = 0
    string = string.lower()
    for char in string:
        if char in vowels:
            count += 1

    return count

input_string = input('Enter a word: ')
print("Number of vowels in the string:", count_vowels(input_string))

